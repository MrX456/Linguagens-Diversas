//Utilização do laço for em JavaScript
var i;
//Ele é composto por valor inicial da variavel, condição para encerrar o loop
//e nesse  caso incrementação para atingir a condição e de encerramento do loop
//(senão teriamos um loop infinito ja que a variavel nunca atingiria seu valor limite)
for (i = 0; i <= 10; i++) {
    document.write(i + "  ");
}
//Decrementando variavel
var j;
document.write("------------");
for (j = 10; j >= 0; j--) {
    document.write(j + "  ");
}