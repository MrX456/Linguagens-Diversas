//A incrementação de variavel acrescenta 1 ao valor da mesma
//Temos pré e pós incrementação

var x = 1;
var y = 2;

document.write("Variavel Declarada : " +x +"<br/>");
document.write("Pos-incremetação(x++) : " + x++ +"<br/>");
//Variavel permanece com mesmo valor porque incremento foi feito depois de mostra-la
document.write("Incremento agora funcionou : " + x++ +"<br/>");
document.write("-------------------------------------------" +"<br/>");
document.write("Variavel Declarada : " +y +"<br/>");
document.write("Pré-incremetação(++y) : " + ++y +"<br/>");
//Variavel ja recebeu Incrementação antes de ser exibida.







