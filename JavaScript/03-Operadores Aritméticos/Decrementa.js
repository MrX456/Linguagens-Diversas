 //Oprerador == verifica se variaveis são iguais
 document.write(x +" = " +y +"  ");
 document.write(x == y);
 document.write("<br/>");
 //Esse operador vai retornar false porque variaveis tem valores diferentes

 //Oprerador === verifica se variaveis são iguais e do mesmo tipo
 document.write(x +" identico a " +z +"  ");
 document.write(x === z);
 document.write("<br/>");
 //Esse operador vai retornar false porque embora variaveis sejam 5 uma delas é do tipo string

 //Operador < verifica se a primeira variavel é menor que a segunda
 document.write(x +" < " +y +"  ");
 document.write(x < y);
 document.write("<br/>");
//Esse operador ira retornar false porque a segunda variavel é menor que a primeira

//Operador > verifica se primeira variavel é maior que a segunda
document.write(x +" > " +y +"  ");
document.write(x > y);
document.write("<br/>");
//Esse operador ira retornar true porque a primeira variavel é maior que a segunda






