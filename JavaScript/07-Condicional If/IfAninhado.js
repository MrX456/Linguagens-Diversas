 //Usando IF aninhado (ELSE IF) para testar varias condições de uma vez
 var valor = 10;
 if (valor == 8) {
     document.write("Valor 8");
 } 
 else if (valor == 6) {
     document.write("Valor 6");
 }
 else if (valor == 10) {
     document.write("Valor 10");//Condição Satisfeita
 }
 else {
     document.write("Valor Desconhecido")
 }