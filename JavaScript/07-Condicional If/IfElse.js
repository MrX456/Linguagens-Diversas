//Estrutura condicional IF ELSE
//Usamos == para verificar se valor de variaveis são iguais
//&& operador logico E (retorna verdadeiro somente se duas condições forem satisfeitas)
var valor1 = 5;
var valor2 = 6;
if (valor1 == 5 && valor2 == 6) {
    document.write("Verdadeiro!");//Condição Satisfeita
} else {
    document.write("Falso!");
}
// || operador logico OU(retorna verdadeiro se apenas uma das condições for satisfeita)
var valor3 = 4;
var valor4 = 3;
if (valor3 == 5 || valor4 == 6) {
    document.write("Verdadeiro!");
} else {
    document.write("Falso!");//Condição Satisfeita
}