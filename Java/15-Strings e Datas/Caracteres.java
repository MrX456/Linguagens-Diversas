class Caracteres {
    public static void main(String[] args) {
        // Caracteres de escape

        // Para quebra uma linha dentro deu cadeia de caracteres usamos \n
        System.out.println("Quebra \nde linha");

        // \t indica tabulação
        System.out.println("1 \t 2 \t 3");

        // \\ permite o uso de \ dentro da String
        System.out.println("12\\07\\2019");

        // \' permite uso de aspa dentro da String
        System.out.println("\'1\'");

        // \" permite uso de aspas dentro da String
        System.out.println("\"1\"");

        // \b uso de backspace(apaga letra anterior)
        System.out.println("abc\bdefg");

    }
}