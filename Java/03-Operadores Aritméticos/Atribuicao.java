class Atribuicao {
    public static void main(String[] args) {
        // Operadores de atribução em java
        int x = 5;
        int y = 3;
        x += 2; // x = x+2
        y -= 3; // y = y-3

        System.out.println(x);
        System.out.println(y);

        // Todos demais operadores podem ser usados com atribuição(*= /= %=)
    }
}