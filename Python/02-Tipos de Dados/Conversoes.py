#podemos converter um tipo de variavel para outro

#str para int
x = '5'
y = int(x)
print(type(y))

#int para str
a = 3
b = str(a)
print(type(b))

#float para str
k = 2.345
l = str(k)
print(type(l))

#str para float
m = '3.421'
n = float(m)
print(type(n))

#float para int (trunca numero na parte inteira)
c = 2.7985
