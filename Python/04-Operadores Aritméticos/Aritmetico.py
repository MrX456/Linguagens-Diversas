#operadores aritméticos em python

a = 5
b = 2

#soma
soma = a + b

#subtração
sub = a - b

#multiplicação
multi = a * b

#divisão
div = a / b

#potenciação
pot = a ** b

#divisão inteira(mostra somente parte inteira do resultado)
divint = a // b

#módulo(resto da divisão dos valores)
resto = a % b

#Temos métodos para fazer outras operações matematicas
#potencias também podem ser feitas dessa forma
pot2 = pow(a,b)

#obs; o Python não possui incrementação ++ ou --

#exibindo resultados
print('Soma')
print(soma)
print('Subtração')
print(sub)
print('Multiplicação')
print(multi)
print('Divisão')
print(div)
print('Potenciação')
print(pot)
print('Divisão Inteira')
print(divint)
print('Resto')
print(resto)
print('Potencia(pow)')
print(pot2)


