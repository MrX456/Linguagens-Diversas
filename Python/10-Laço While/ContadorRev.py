#Podemos fazer repetições em python através do laço while
#Vamos fazer um contador reverso de 10 a 0

cont = 10


#O while testa a condição e se for verdadeira ele sera excecutado
#diversas vezes ate que a condição se torne falsa

#Enquanto variavel cont for maior ou igual a 0, cont diminui em 1
while cont >= 0 :
    print(cont)
    cont -= 1

#Quando cont valer -1 este bloco não será mais executado
