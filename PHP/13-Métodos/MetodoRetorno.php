<?php

    //Métodos que possuem a palavra return, retornam algo ao chamador
    function ola() {

        return "Ola Mundo !";

    }

    //Sabemos que o método retorna uma string "Ola Mundo !"
    //Vamos usar um comando echo para verificar
    echo ola();
