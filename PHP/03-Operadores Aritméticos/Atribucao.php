<?php

//Operadores de atribuição permitem escrever expressões de maneira mais simples
//os operadores de atribuição em php são += -= *= /= %=

$w = 2;
$w += 3; // $w = $w + 3 -> 2 + 3

$x = 4;
$x -= 3; // $x = $x - 3 -> 4 - 3

$y = 2;
$y *= 4; // $y = $y * 4 -> 2 * 4

$z = 8;
$z /= 4; // $z = $z / 4 -> 8 / 4

$r = 5;
$r %= 2; // $r = $r % 2 -> 5 % 2

echo $w;
echo'<br/>';
echo $x;
echo'<br/>';
echo $y;
echo'<br/>';
echo $z;
echo'<br/>';
echo $r;
echo'<br/>';

?>